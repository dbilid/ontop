   4 AS "mQuestType", NULL AS "mLang", CAST(QVIEW1."PRFMONTH" AS VARCHAR(500)) AS "m", 
   5 AS "oQuestType", NULL AS "oLang", CAST(QVIEW1."PRFPRDOILNETMILLSM3" AS VARCHAR(500)) AS "o"
 FROM 
"PUBLIC"."FIELD_PRODUCTION_MONTHLY" QVIEW1
WHERE 
QVIEW1."PRFNPDIDINFORMATIONCARRIER" IS NOT NULL AND
QVIEW1."PRFYEAR" IS NOT NULL AND
QVIEW1."PRFMONTH" IS NOT NULL AND
QVIEW1."PRFPRDOILNETMILLSM3" IS NOT NULL AND
QVIEW1."PRFPRDGASNETBILLSM3" IS NOT NULL AND
(QVIEW1."PRFYEAR" > 1999) AND
((QVIEW1."PRFMONTH" >= 1) AND (QVIEW1."PRFMONTH" <= 6))
UNION
SELECT 
   4 AS "yearQuestType", NULL AS "yearLang", CAST(QVIEW1."PRFYEAR" AS VARCHAR(500)) AS "year", 
   5 AS "gQuestType", NULL AS "gLang", CAST(QVIEW4."PRFPRDGASNETBILLSM3" AS VARCHAR(500)) AS "g", 
   4 AS "mQuestType", NULL AS "mLang", CAST(QVIEW1."PRFMONTH" AS VARCHAR(500)) AS "m", 
   5 AS "oQuestType", NULL AS "oLang", CAST(QVIEW2."PRFPRDOILNETMILLSM3" AS VARCHAR(500)) AS "o"
 FROM 
"PUBLIC"."FIELD_PRODUCTION_TOTALT_NCS_MONTH" QVIEW1,
"PUBLIC"."FIELD_PRODUCTION_TOTALT_NCS_MONTH" QVIEW2,
"PUBLIC"."FIELD_PRODUCTION_TOTALT_NCS_MONTH" QVIEW3,
"PUBLIC"."FIELD_PRODUCTION_TOTALT_NCS_MONTH" QVIEW4
