(QVIEW1."PRFMONTH" = QVIEW3."PRFMONTH") AND
(QVIEW1."PRFYEAR" = QVIEW4."PRFYEAR") AND
(QVIEW1."PRFMONTH" = QVIEW4."PRFMONTH") AND
