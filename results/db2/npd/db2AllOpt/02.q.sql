SELECT *
FROM (
SELECT

   8 AS "dateQuestType", NULL AS "dateLang", CAST(var0 AS VARCHAR(500)) AS "date", 
   1 AS "licenceURIQuestType", NULL AS "licenceURILang", CAST(('http://sws.ifi.uio.no/data/npd-v2/licence/' || REPLACE(REPLACE(REPLACE(REPLACE(REPLACE(REPLACE(REPLACE(REPLACE(REPLACE(REPLACE(REPLACE(REPLACE(REPLACE(REPLACE(REPLACE(REPLACE(REPLACE(REPLACE(REPLACE(CAST(var1 AS VARCHAR(500)), ' ', '%20'), '!', '%21'), '''', '%22'), '#', '%23'), '$', '%24'), '&', '%26'), '(', '%28'), ')', '%29'), '*', '%2A'), '+', '%2B'), ',', '%2C'), '/', '%2F'), ':', '%3A'), ';', '%3B'), '=', '%3D'), '?', '%3F'), '@', '%40'), '[', '%5B'), ']', '%5D')) AS VARCHAR(500)) AS "licenceURI", 
   7 AS "companyQuestType", NULL AS "companyLang", CAST(var2 AS VARCHAR(500)) AS "company"
FROM (
 (
SELECT DISTINCT 
   QVIEW1."PRLOPERDATEVALIDFROM" as var0, 
   QVIEW1."PRLNPDIDLICENCE" as var1, 
   QVIEW2."CMPLONGNAME" as var2
 FROM 
"PUBLIC"."LICENCE_OPER_HST" QVIEW1,
"PUBLIC"."COMPANY" QVIEW2,
"PUBLIC"."LICENCE" QVIEW3
WHERE 
(QVIEW1."CMPNPDIDCOMPANY" = QVIEW2."CMPNPDIDCOMPANY") AND
QVIEW1."PRLNPDIDLICENCE" IS NOT NULL AND
QVIEW1."CMPNPDIDCOMPANY" IS NOT NULL AND
QVIEW1."PRLOPERDATEVALIDFROM" IS NOT NULL AND
QVIEW1."PRLOPERDATEVALIDTO" IS NOT NULL AND
QVIEW2."CMPLONGNAME" IS NOT NULL AND
(QVIEW1."PRLNPDIDLICENCE" = QVIEW3."PRLNPDIDLICENCE") AND
('9999-12-31' <> QVIEW1."PRLOPERDATEVALIDFROM") AND
(QVIEW1."PRLOPERDATEVALIDFROM" > '1979-12-31 00:00:00')
 ) 
 
 ) SUBQALIAS
) SUB_QVIEW
ORDER BY SUB_QVIEW."licenceURI"

