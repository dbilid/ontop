SELECT *
FROM (
SELECT

   1 AS "frQuestType", NULL AS "frLang", ('http://sws.ifi.uio.no/data/npd-v2/field/' || REPLACE(REPLACE(REPLACE(REPLACE(REPLACE(REPLACE(REPLACE(REPLACE(REPLACE(REPLACE(REPLACE(REPLACE(REPLACE(REPLACE(REPLACE(REPLACE(REPLACE(REPLACE(REPLACE(CAST(var0 AS VARCHAR(4000)), ' ', '%20'), '!', '%21'), '''', '%22'), '#', '%23'), '$', '%24'), '&', '%26'), '(', '%28'), ')', '%29'), '*', '%2A'), '+', '%2B'), ',', '%2C'), '/', '%2F'), ':', '%3A'), ';', '%3B'), '=', '%3D'), '?', '%3F'), '@', '%40'), '[', '%5B'), ']', '%5D') || '/reserve') AS "fr", 
   5 AS "conQuestType", NULL AS "conLang", CAST(var1 AS VARCHAR(4000)) AS "con", 
   5 AS "gasQuestType", NULL AS "gasLang", CAST(var2 AS VARCHAR(4000)) AS "gas", 
   5 AS "NGLQuestType", NULL AS "NGLLang", CAST(var3 AS VARCHAR(4000)) AS "NGL", 
   5 AS "oilQuestType", NULL AS "oilLang", CAST(var4 AS VARCHAR(4000)) AS "oil", 
   5 AS "OEQuestType", NULL AS "OELang", CAST(var5 AS VARCHAR(4000)) AS "OE"
FROM (
 (
SELECT 
   QVIEW1."FLDNPDIDFIELD" as var0, 
   QVIEW1."FLDREMAININGCONDENSATE" as var1, 
   QVIEW1."FLDREMAININGGAS" as var2, 
   QVIEW1."FLDREMAININGNGL" as var3, 
   QVIEW1."FLDREMAININGOIL" as var4, 
   QVIEW1."FLDREMAININGOE" as var5
 FROM 
"NPD"."FIELD_RESERVES" QVIEW1
WHERE 
QVIEW1."FLDREMAININGGAS" IS NOT NULL AND
QVIEW1."FLDNPDIDFIELD" IS NOT NULL AND
QVIEW1."FLDREMAININGOE" IS NOT NULL AND
QVIEW1."FLDREMAININGNGL" IS NOT NULL AND
QVIEW1."FLDREMAININGCONDENSATE" IS NOT NULL AND
QVIEW1."FLDREMAININGOIL" IS NOT NULL AND
(QVIEW1."FLDREMAININGGAS" < 100)
 ) 
 
 ) SUBQALIAS
) SUB_QVIEW

ORDER BY SUB_QVIEW."OE" DESC
