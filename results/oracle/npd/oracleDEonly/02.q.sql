SELECT *
FROM (
SELECT DISTINCT 
   8 AS "dateQuestType", NULL AS "dateLang", CAST(QVIEW1."PRLOPERDATEVALIDFROM" AS VARCHAR(4000)) AS "date", 
   1 AS "licenceURIQuestType", NULL AS "licenceURILang", ('http://sws.ifi.uio.no/data/npd-v2/licence/' || REPLACE(REPLACE(REPLACE(REPLACE(REPLACE(REPLACE(REPLACE(REPLACE(REPLACE(REPLACE(REPLACE(REPLACE(REPLACE(REPLACE(REPLACE(REPLACE(REPLACE(REPLACE(REPLACE(CAST(QVIEW1."PRLNPDIDLICENCE" AS VARCHAR(4000)), ' ', '%20'), '!', '%21'), '''', '%22'), '#', '%23'), '$', '%24'), '&', '%26'), '(', '%28'), ')', '%29'), '*', '%2A'), '+', '%2B'), ',', '%2C'), '/', '%2F'), ':', '%3A'), ';', '%3B'), '=', '%3D'), '?', '%3F'), '@', '%40'), '[', '%5B'), ']', '%5D')) AS "licenceURI", 
   7 AS "companyQuestType", NULL AS "companyLang", QVIEW2."CMPLONGNAME" AS "company"
 FROM 
"NPD"."LICENCE_OPER_HST" QVIEW1,
"NPD"."COMPANY" QVIEW2
WHERE 
QVIEW1."PRLNPDIDLICENCE" IS NOT NULL AND
QVIEW1."CMPNPDIDCOMPANY" IS NOT NULL AND
QVIEW1."PRLOPERDATEVALIDFROM" IS NOT NULL AND
QVIEW1."PRLOPERDATEVALIDTO" IS NOT NULL AND
(QVIEW1."CMPNPDIDCOMPANY" = QVIEW2."CMPNPDIDCOMPANY") AND
QVIEW2."CMPLONGNAME" IS NOT NULL AND
('31-dec-9999' <> QVIEW1."PRLOPERDATEVALIDFROM") AND
(QVIEW1."PRLOPERDATEVALIDFROM" > to_timestamp('1979-12-31 00:00:00','YYYY-MM-DD HH24:MI:SS'))
) SUB_QVIEW

ORDER BY SUB_QVIEW."licenceURI"
